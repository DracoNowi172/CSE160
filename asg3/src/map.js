// Function to place random blocks with unique textures
function placeRandomBlocks(numberOfBlocks, textures) {
    let emptySpaces = [];
    for (let x = 0; x < g_map.length; x++) {
        for (let z = 0; z < g_map[x].length; z++) {
            if (g_map[x][z] === 0) {
                emptySpaces.push({x, z});
            }
        }
    }
    shuffleArray(emptySpaces);
    const selectedSpaces = emptySpaces.slice(0, numberOfBlocks);
    selectedSpaces.forEach((space, index) => {
        let block = new Cube();
        block.textureNum = textures[index % textures.length];
        block.translate(space.x - 15.5, 0.01, space.z - 15.5);
        randomBlocks.push(block);
        g_map[space.x][space.z] = 2; // Update map to reflect these are special blocks
    });
}

// Utility function to shuffle an array (Fisher-Yates shuffle)
function shuffleArray(array){
    for (let i = array.length - 1; i > 0; i--){
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]]; // Swap elements
    }
}

