var walls = []; // This will only hold boundary walls
var randomBlocks = []; // This will hold randomly placed blocks

function createWorld() {
    for (let x = 0; x < g_map.length; x++) {
        for (let z = 0; z < g_map[x].length; z++) {
            if (g_map[x][z] === 1) { // Only boundary walls are '1'
                let wall = new Cube();
                wall.textureNum = 6; // Texture for walls
                wall.translate(x - 15.5, 0.01, z - 15.5);
                walls.push(wall);
            }
        }
    }
    //placeRandomBlocks(10, [5, 6]); // Place random blocks after walls are placed
    placeRandomBlocks(10, [5, 6]); // Place 10 blocks, using texture indices
    startTimer(); // Start the timer after all blocks are placed
}
